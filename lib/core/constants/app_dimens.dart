/// A class that holds all the dimension values for the app.
class AppDimens {
  /// A small gap, used for minor spacing.
  static const double smallGap = 8.0;

  /// A medium gap, used for default spacing between elements.
  static const double mediumGap = 16.0;

  /// A large gap, used for major spacing between sections.
  static const double largeGap = 24.0;

  /// The radius for card elements.
  static const double cardRadius = 16.0;

  /// The radius for button elements.
  static const double buttonRadius = 12.0;
}